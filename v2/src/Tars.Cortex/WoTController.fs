/// <summary>
/// Phase 8.4.1: WoT Controller - The cognitive orchestrator for Workflow of Thought.
///
/// Implements the five key controller components:
/// 1. Planner - Breaks goal into sub-goals and seeds initial nodes
/// 2. Generator - Creates candidate thoughts, tool calls, summaries
/// 3. Critic - Evaluates relevance, correctness, risk, policy compliance
/// 4. Router - Decides: expand, merge, rollback, escalate, or finalize
/// 5. Distiller - Compresses subgraph into clean prose or structured JSON
/// </summary>
module WoTController =

    // =========================================================================
    // Router Decision Types
    // =========================================================================

    /// <summary>
    /// Decisions the Router can make during WoT execution.
    /// </summary>
    type RouterDecision =
        | Expand of nodeId: string * reason: string
        | Merge of nodeIds: string list * targetSynthesis: string
        | Rollback of toNodeId: string * reason: string
        | Escalate of nodeId: string * reason: string * urgency: float
        | Finalize of nodeId: string * confidence: float
        | CallTool of toolName: string * args: Map<string, obj>
        | ApplyPolicy of policyName: string * target: string
        | Retry of nodeId: string * maxAttempts: int

    /// <summary>
    /// Context for routing decisions.
    /// </summary>
    type RoutingContext =
        { CurrentNode: WoTNode
          ExecutedNodes: WoTTraceStep list
          RemainingBudget: Budget
          ConstraintScore: float option
          FailedAttempts: Map<string, int>
          Goal: string }

    // =========================================================================
    // Critic Evaluation Types
    // =========================================================================

    /// <summary>
    /// Dimensions the Critic evaluates.
    /// </summary>
    type CriticDimension =
        | Relevance of score: float * reason: string
        | Correctness of score: float * evidence: string list
        | Risk of level: float * factors: string list
        | PolicyCompliance of passed: bool * violations: string list
        | Quality of score: float * aspects: Map<string, float>

    /// <summary>
    /// Complete critique of a node or subgraph.
    /// </summary>
    type Critique =
        { NodeId: string
          Dimensions: CriticDimension list
          OverallScore: float
          Recommendation: RouterDecision
          Reasoning: string
          CritiquedAt: DateTime }

    // =========================================================================
    // Planner Types
    // =========================================================================

