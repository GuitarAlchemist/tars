    /// <summary>
    /// The Planner decomposes goals into sub-goals and seeds initial nodes.
    /// </summary>
    module Planner =

        /// <summary>
        /// Decompose a goal into sub-goals using heuristic analysis.
        /// </summary>
        let decompose (goal: string) : SubGoal list =
            // Identify complexity markers
            let hasMultipleParts =
                goal.Contains(" and ") || goal.Contains(" then ") || goal.Contains(";")

            let requiresResearch =
                goal.Contains("analyze")
                || goal.Contains("research")
                || goal.Contains("investigate")

            let requiresImplementation =
                goal.Contains("implement") || goal.Contains("create") || goal.Contains("build")

            let requiresVerification =
                goal.Contains("verify") || goal.Contains("validate") || goal.Contains("test")

            // Generate sub-goals based on analysis
            [ if requiresResearch then
                  yield
                      { Id = "sg_research"
                        Description = $"Research: Gather information about {goal}"
                        Priority = 1
                        Dependencies = []
                        EstimatedComplexity = 0.3
                        SuggestedPattern = Some GraphOfThoughts }

              if requiresImplementation then
                  yield
                      { Id = "sg_implement"
                        Description = $"Implement: Execute the core task"
                        Priority = 2
                        Dependencies = if requiresResearch then [ "sg_research" ] else []
                        EstimatedComplexity = 0.5
                        SuggestedPattern = Some ReAct }

              if requiresVerification then
                  yield
                      { Id = "sg_verify"
                        Description = $"Verify: Validate the result"
                        Priority = 3
                        Dependencies = if requiresImplementation then [ "sg_implement" ] else []
                        EstimatedComplexity = 0.2
                        SuggestedPattern = Some ChainOfThought }

              if not requiresResearch && not requiresImplementation && not requiresVerification then
                  yield
                      { Id = "sg_main"
                        Description = goal
                        Priority = 1
                        Dependencies = []
                        EstimatedComplexity = 0.5
                        SuggestedPattern = Some ChainOfThought } ]

        /// <summary>
        /// Seed initial WoT nodes from sub-goals.
        /// </summary>
        let seedNodes (subGoals: SubGoal list) : WoTNode list * WoTEdge list =
            let nodes =
                subGoals
                |> List.map (fun sg ->
                    { Id = $"plan_{sg.Id}"
                      Kind = Reason
                      Payload =
                        { Prompt = sg.Description
                          Hint = Some Smart }
                        :> obj
                      Metadata =
                        { Label = Some sg.Description
                          Tags = [ "planner"; "subgoal" ]
                          Extra = Map.empty } })

            let edges =
                subGoals
                |> List.collect (fun sg ->
                    sg.Dependencies
                    |> List.map (fun dep ->
                        { From = $"plan_{dep}"
                          To = $"plan_{sg.Id}"
                          Label = Some "depends_on"
                          Confidence = Some 1.0 }))

            (nodes, edges)

        /// <summary>
        /// Create a complete plan for a goal.
        /// </summary>
        let createPlan (goal: string) (budget: Budget) : PlannerOutput =
            let subGoals = decompose goal
            let (nodes, edges) = seedNodes subGoals

            let estimatedSteps =
                subGoals
                |> List.sumBy (fun sg ->
                    match sg.SuggestedPattern with
                    | Some ChainOfThought -> 3
                    | Some ReAct -> 5
                    | Some GraphOfThoughts -> 6
                    | Some TreeOfThoughts -> 8
                    | _ -> 3)

            { Goal = goal
              SubGoals = subGoals
              InitialNodes = nodes
              InitialEdges = edges
              EstimatedSteps = estimatedSteps
              EstimatedTokens = estimatedSteps * 500 // ~500 tokens per step
              Constraints = [ "max_retries:3"; "require_verification" ] }

