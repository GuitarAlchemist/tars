    /// <summary>
    /// Initialize controller state for a goal.
    /// </summary>
    let init (goal: string) (budget: Budget) : ControllerState =
        let plannerOutput = Planner.createPlan goal budget

        let plan =
            { Id = Guid.NewGuid()
              Nodes = plannerOutput.InitialNodes
              Edges = plannerOutput.InitialEdges
              EntryNode =
                plannerOutput.InitialNodes
                |> List.tryHead
                |> Option.map (fun n -> n.Id)
                |> Option.defaultValue ""
              Metadata =
                { Kind = WorkflowOfThought
                  SourceGoal = goal
                  CompiledAt = DateTime.UtcNow
                  EstimatedTokens = Some plannerOutput.EstimatedTokens
                  EstimatedSteps = Some plannerOutput.EstimatedSteps }
              Policy = plannerOutput.Constraints }

        { Plan = plan
          ExecutedSteps = []
          PendingNodes = plannerOutput.InitialNodes |> List.map (fun n -> n.Id)
          ActiveNode = Some plan.EntryNode
          Critiques = Map.empty
          Candidates = []
          Decisions = []
          Budget = budget
          StartedAt = DateTime.UtcNow
          LastActivity = DateTime.UtcNow
          EscalationsPending = []
          HumanInputRequired = false }

    /// <summary>
    /// Execute one step of the controller loop.
    /// </summary>
    let step (state: ControllerState) : ControllerState * RouterDecision option =
        match state.ActiveNode with
        | None ->
            // No active node - finalize
            (state, Some(Finalize("", 1.0)))

        | Some nodeId ->
            // Find the current node
            let nodeOpt = state.Plan.Nodes |> List.tryFind (fun n -> n.Id = nodeId)

            match nodeOpt with
            | None ->
                // Node not found, move to next
                let remaining = state.PendingNodes |> List.filter (fun id -> id <> nodeId)
                let newActive = remaining |> List.tryHead

                ({ state with
                    PendingNodes = remaining
                    ActiveNode = newActive
                    LastActivity = DateTime.UtcNow },
                 None)

            | Some node ->
                // Critique the current node
                let critique = Critic.critique node state.Plan.Metadata.SourceGoal state.Plan.Policy

                // Create routing context
                let context =
                    { CurrentNode = node
                      ExecutedNodes = state.ExecutedSteps
                      RemainingBudget = state.Budget
                      ConstraintScore = None
                      FailedAttempts =
                        state.ExecutedSteps
                        |> List.filter (fun s ->
                            match s.Status with
                            | Failed _ -> true
                            | _ -> false)
                        |> List.groupBy (fun s -> s.NodeId)
                        |> List.map (fun (id, steps) -> (id, steps.Length))
                        |> Map.ofList
                      Goal = state.Plan.Metadata.SourceGoal }

                // Make routing decision
                let decision = Router.decide context (Some critique)

                // Update state based on decision
                let needsHuman = Router.needsHumanInput decision

                let newState =
                    { state with
                        Critiques = state.Critiques |> Map.add nodeId critique
                        Decisions = (DateTime.UtcNow, decision) :: state.Decisions
                        HumanInputRequired = needsHuman
                        EscalationsPending =
                            match decision with
                            | Escalate(id, reason, urgency) -> (id, reason, urgency) :: state.EscalationsPending
                            | _ -> state.EscalationsPending
                        LastActivity = DateTime.UtcNow }

                (newState, Some decision)

    /// <summary>
    /// Run the full controller loop until complete or escalation.
    /// </summary>
    let run (state: ControllerState) (maxSteps: int) : ControllerState * DistilledOutput =
        let rec loop currentState stepCount =
            if stepCount >= maxSteps then
                currentState
            elif currentState.HumanInputRequired then
                currentState
            elif currentState.ActiveNode.IsNone then
                currentState
            else
                let (newState, decision) = step currentState

                match decision with
                | Some(Finalize _) -> newState
                | Some(Escalate _) -> newState
                | _ -> loop newState (stepCount + 1)

        let finalState = loop state 0

        let distilled =
            Distiller.distill finalState.ExecutedSteps state.Plan.Metadata.SourceGoal Prose

        (finalState, distilled)

