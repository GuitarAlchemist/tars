    module IntegratedRunner =

        /// <summary>
        /// Execute a Reason node using the LLM service.
        /// </summary>
        let private executeReasonNode
            (services: ControllerServices)
            (node: WoTNode)
            (goal: string)
            (context: string list)
            : Async<WoTTraceStep> =
            async {
                let startTime = DateTime.UtcNow
                let sw = System.Diagnostics.Stopwatch.StartNew()

                let prompt =
                    sprintf
                        "Goal: %s\n\nTask: %s\n\nContext:\n%s\n\nProvide a thoughtful response:"
                        goal
                        node.Id
                        (context |> String.concat "\n")

                match services.LlmService with
                | Some llm ->
                    let! result = llm.ReasonAsync(prompt, Some(context |> String.concat "\n"), Some 2000)
                    sw.Stop()

                    match result with
                    | Result.Ok res ->
                        return
                            { NodeId = node.Id
                              NodeType = "Reason"
                              StartedAt = startTime
                              Status = NodeStatus.Completed(res.Response, sw.ElapsedMilliseconds)
                              Input = Some prompt
                              Output = Some res.Response
                              Confidence = Some res.Confidence
                              TokensUsed = Some res.TokensUsed }
                    | Result.Error err ->
                        return
                            { NodeId = node.Id
                              NodeType = "Reason"
                              StartedAt = startTime
                              Status = NodeStatus.Failed(err, sw.ElapsedMilliseconds)
                              Input = Some prompt
                              Output = None
                              Confidence = None
                              TokensUsed = None }
                | None ->
                    sw.Stop()
                    // Fallback to heuristic response
                    let heuristicResponse =
                        sprintf "[Heuristic] Processing node %s for goal: %s" node.Id goal

                    return
                        { NodeId = node.Id
                          NodeType = "Reason"
                          StartedAt = startTime
                          Status = NodeStatus.Completed(heuristicResponse, sw.ElapsedMilliseconds)
                          Input = Some prompt
                          Output = Some heuristicResponse
                          Confidence = Some 0.5
                          TokensUsed = Some 0 }
            }

        /// <summary>
        /// Execute a Tool node using the tool service.
        /// </summary>
        let private executeToolNode
            (services: ControllerServices)
            (node: WoTNode)
            (toolName: string)
            (args: Map<string, obj>)
            : Async<WoTTraceStep> =
            async {
                let startTime = DateTime.UtcNow
                let sw = System.Diagnostics.Stopwatch.StartNew()

                match services.ToolService with
                | Some toolSvc ->
                    let! result = toolSvc.ExecuteAsync(toolName, args)
                    sw.Stop()

                    match result with
                    | Result.Ok res ->
                        return
                            { NodeId = node.Id
                              NodeType = "Tool"
                              StartedAt = startTime
                              Status =
                                if res.Success then
                                    NodeStatus.Completed(res.Output, res.DurationMs)
                                else
                                    NodeStatus.Failed(res.Output, res.DurationMs)
                              Input = Some(sprintf "Tool: %s, Args: %A" toolName args)
                              Output = Some res.Output
                              Confidence = if res.Success then Some 1.0 else Some 0.0
                              TokensUsed = None }
                    | Result.Error err ->
                        return
                            { NodeId = node.Id
                              NodeType = "Tool"
                              StartedAt = startTime
                              Status = NodeStatus.Failed(err, sw.ElapsedMilliseconds)
                              Input = Some(sprintf "Tool: %s" toolName)
                              Output = None
                              Confidence = None
                              TokensUsed = None }
                | None ->
                    sw.Stop()
                    let mockOutput = sprintf "[Mock] Tool %s executed with args %A" toolName args

                    return
                        { NodeId = node.Id
                          NodeType = "Tool"
                          StartedAt = startTime
                          Status = NodeStatus.Completed(mockOutput, sw.ElapsedMilliseconds)
                          Input = Some(sprintf "Tool: %s" toolName)
                          Output = Some mockOutput
                          Confidence = Some 0.5
                          TokensUsed = None }
            }

        /// <summary>
        /// Execute a Knowledge operation.
        /// </summary>
        let private executeKnowledgeOp
            (services: ControllerServices)
            (op: MemoryOp)
            (nodeId: string)
            : Async<WoTTraceStep> =
