                let startTime = DateTime.UtcNow
                let sw = System.Diagnostics.Stopwatch.StartNew()

                match services.KnowledgeService with
                | Some kSvc ->
                    let knowledgeOp =
                        match op with
                        | MemoryOp.Query sparql -> QueryTriples(sparql)
                        | MemoryOp.Assert(s, p, o) -> AssertFact(s, p, o)
                        | MemoryOp.Retract(s, p, o) -> RetractFact(s, p, o)
                        | MemoryOp.Search(emb, k) -> SearchKnowledge("vector_search_placeholder", k)

                    let! result = kSvc.ExecuteOpAsync(knowledgeOp)
                    sw.Stop()

                    match result with
                    | Result.Ok output ->
                        return
                            { NodeId = nodeId
                              NodeType = "Memory"
                              StartedAt = startTime
                              Status = NodeStatus.Completed(output, sw.ElapsedMilliseconds)
                              Input = Some(sprintf "MemoryOp: %A" op)
                              Output = Some output
                              Confidence = Some 1.0
                              TokensUsed = None }
                    | Result.Error err ->
                        return
                            { NodeId = nodeId
                              NodeType = "Memory"
                              StartedAt = startTime
                              Status = NodeStatus.Failed(err, sw.ElapsedMilliseconds)
                              Input = Some(sprintf "MemoryOp: %A" op)
                              Output = None
                              Confidence = None
                              TokensUsed = None }
                | None ->
                    sw.Stop()
                    let mockOutput = sprintf "[Mock] Knowledge op: %A" op

                    return
                        { NodeId = nodeId
                          NodeType = "Memory"
                          StartedAt = startTime
                          Status = NodeStatus.Completed(mockOutput, sw.ElapsedMilliseconds)
                          Input = Some(sprintf "MemoryOp: %A" op)
                          Output = Some mockOutput
                          Confidence = Some 0.5
                          TokensUsed = None }
            }

        /// <summary>
        /// Handle human escalation.
        /// </summary>
        let private handleEscalation
            (services: ControllerServices)
            (nodeId: string)
            (reason: string)
            (urgency: float)
            (context: string)
            : Async<EscalationResponse> =
            async {
                match services.EscalationService with
                | Some escSvc ->
                    let request =
                        { NodeId = nodeId
                          Reason = reason
                          Urgency = urgency
                          Context = context
                          Options = [ "continue"; "skip"; "abort"; "modify" ]
                          DefaultOption = Some "continue" }

                    return! escSvc.RequestInputAsync(request)
                | None ->
                    // Auto-approve if no escalation service
                    return
                        { SelectedOption = Some "continue"
                          FreeformInput = None
                          Approved = true
                          Timestamp = DateTime.UtcNow }
            }

        /// <summary>
        /// Execute validation on content.
        /// </summary>
        let private executeValidation
            (services: ControllerServices)
            (nodeId: string)
            (content: string)
            (invariants: WoTInvariant list)
            : Async<WoTTraceStep> =
            async {
                let startTime = DateTime.UtcNow
                let sw = System.Diagnostics.Stopwatch.StartNew()

                match services.ValidationService with
                | Some valSvc ->
                    let! result =
                        if invariants.IsEmpty then
                            valSvc.ValidateAsync(content, "general")
                        else
                            valSvc.ValidateInvariantsAsync(content, invariants)

                    sw.Stop()

                    match result with
                    | Ok res ->
                        let output =
                            sprintf
                                "Validation %s: %s (Score: %.2f)"
                                (if res.Passed then "PASSED" else "FAILED")
                                res.ValidatorName
                                res.Score

                        return
                            { NodeId = nodeId
                              NodeType = "Validate"
                              StartedAt = startTime
                              Status =
                                if res.Passed then
                                    NodeStatus.Completed(output, sw.ElapsedMilliseconds)
                                else
                                    NodeStatus.Failed(output, sw.ElapsedMilliseconds)
                              Input = Some content
                              Output = Some output
                              Confidence = Some res.Score
                              TokensUsed = None }
                    | Result.Error err ->
                        return
                            { NodeId = nodeId
                              NodeType = "Validate"
                              StartedAt = startTime
                              Status = NodeStatus.Failed(err, sw.ElapsedMilliseconds)
                              Input = Some content
                              Output = None
                              Confidence = None
                              TokensUsed = None }
                | None ->
                    sw.Stop()

                    let mockOutput =
                        sprintf "[Mock] Validation passed for %d invariants" invariants.Length

                    return
                        { NodeId = nodeId
                          NodeType = "Validate"
                          StartedAt = startTime
                          Status = NodeStatus.Completed(mockOutput, sw.ElapsedMilliseconds)
                          Input = Some content
                          Output = Some mockOutput
                          Confidence = Some 0.8
                          TokensUsed = None }
            }

        /// <summary>
        /// Execute a single node with full service integration.
        /// </summary>
        let executeNode
            (services: ControllerServices)
            (state: ControllerState)
            (node: WoTNode)
            : Async<ControllerState * WoTTraceStep> =
            async {
                // Get context from previous steps
                let context =
                    state.ExecutedSteps |> List.choose (fun s -> s.Output) |> List.truncate 5

                let! traceStep =
                    match node.Kind with
                    | Reason -> executeReasonNode services node state.Plan.Metadata.SourceGoal context
                    | Tool ->
                        // Extract tool name from node metadata or ID
                        let toolName = node.Id.Replace("tool_", "")
                        executeToolNode services node toolName Map.empty
                    | Validate ->
                        let content = context |> String.concat "\n"

                        let invariants =
                            match node.Payload with
                            | :? ValidatePayload as p -> p.Invariants
                            | _ -> []

                        executeValidation services node.Id content invariants
                    | Memory ->
                        let query = sprintf "DESCRIBE <%s>" node.Id
                        executeKnowledgeOp services (MemoryOp.Query query) node.Id
                    | Control ->
                        async {
                            return
                                { NodeId = node.Id
                                  NodeType = "Control"
                                  StartedAt = DateTime.UtcNow
                                  Status = NodeStatus.Completed("Control node executed", 0L)
                                  Input = None
                                  Output = Some "Control flow processed"
                                  Confidence = Some 1.0
                                  TokensUsed = None }
                        }

                // Update state with new step
                let newState =
                    { state with
                        ExecutedSteps = traceStep :: state.ExecutedSteps
                        LastActivity = DateTime.UtcNow }

                return (newState, traceStep)
            }

        /// <summary>
        /// Run one integrated step of the controller.
        /// </summary>
        let stepIntegrated
            (services: ControllerServices)
            (state: ControllerState)
            : Async<ControllerState * RouterDecision option> =
            async {
                match state.ActiveNode with
                | None -> return (state, Some(Finalize("", 1.0)))

                | Some nodeId ->
                    let nodeOpt = state.Plan.Nodes |> List.tryFind (fun n -> n.Id = nodeId)

                    match nodeOpt with
                    | None ->
                        let remaining = state.PendingNodes |> List.filter (fun id -> id <> nodeId)
                        let newActive = remaining |> List.tryHead

                        return
                            ({ state with
                                PendingNodes = remaining
                                ActiveNode = newActive
                                LastActivity = DateTime.UtcNow },
                             None)

                    | Some node ->
                        // Execute the node
                        let! (stateAfterExec, traceStep) = executeNode services state node

                        // Critique the result
                        let critique = Critic.critique node state.Plan.Metadata.SourceGoal state.Plan.Policy

                        // Create routing context
                        let context =
                            { CurrentNode = node
                              ExecutedNodes = stateAfterExec.ExecutedSteps
                              RemainingBudget = state.Budget
                              ConstraintScore = None
                              FailedAttempts =
                                stateAfterExec.ExecutedSteps
                                |> List.filter (fun s ->
                                    match s.Status with
                                    | NodeStatus.Failed _ -> true
                                    | _ -> false)
                                |> List.groupBy (fun s -> s.NodeId)
                                |> List.map (fun (id, steps) -> (id, steps.Length))
                                |> Map.ofList
                              Goal = state.Plan.Metadata.SourceGoal }

                        // Make routing decision
                        let decision = Router.decide context (Some critique)

                        // Handle escalation if needed
                        let! needsHuman =
                            async {
                                match decision with
                                | Escalate(escNodeId, reason, urgency) ->
                                    let! response =
                                        handleEscalation
                                            services
                                            escNodeId
                                            reason
                                            urgency
                                            (stateAfterExec.ExecutedSteps
                                             |> List.tryHead
                                             |> Option.bind (fun s -> s.Output)
                                             |> Option.defaultValue "")

                                    return not response.Approved
                                | _ -> return false
                            }

                        // Move to next node
                        let remaining = stateAfterExec.PendingNodes |> List.filter (fun id -> id <> nodeId)
                        let newActive = remaining |> List.tryHead

                        let newState =
                            { stateAfterExec with
                                PendingNodes = remaining
                                ActiveNode = newActive
                                Critiques = stateAfterExec.Critiques |> Map.add nodeId critique
                                Decisions = (DateTime.UtcNow, decision) :: stateAfterExec.Decisions
                                HumanInputRequired = needsHuman
                                EscalationsPending =
                                    match decision with
                                    | Escalate(id, reason, urgency) ->
                                        (id, reason, urgency) :: stateAfterExec.EscalationsPending
                                    | _ -> stateAfterExec.EscalationsPending
                                LastActivity = DateTime.UtcNow }

                        return (newState, Some decision)
            }

        /// <summary>
        /// Run the full integrated controller loop.
        /// </summary>
        let runIntegrated
            (services: ControllerServices)
            (state: ControllerState)
            (maxSteps: int)
            : Async<ControllerState * DistilledOutput> =
            async {
                let rec loop currentState stepCount =
                    async {
                        if stepCount >= maxSteps then
                            return currentState
                        elif currentState.HumanInputRequired then
                            return currentState
                        elif currentState.ActiveNode.IsNone then
                            return currentState
                        else
                            let! (newState, decision) = stepIntegrated services currentState

                            match decision with
                            | Some(Finalize _) -> return newState
                            | Some(Escalate _) when newState.HumanInputRequired -> return newState
                            | _ -> return! loop newState (stepCount + 1)
                    }

                let! finalState = loop state 0

                // Log insights to knowledge ledger if available
                match services.KnowledgeService with
                | Some kSvc ->
                    let insights = Distiller.extractInsights finalState.ExecutedSteps

                    for insight in insights do
                        do! kSvc.LogInsight(insight, "WoT Controller")
                | None -> ()

                let distilled =
                    Distiller.distill finalState.ExecutedSteps state.Plan.Metadata.SourceGoal Prose

                return (finalState, distilled)
            }

        /// <summary>
        /// Create a fully integrated controller and run it.
        /// </summary>
        let executeGoal
            (services: ControllerServices)
            (goal: string)
            (budget: Budget)
            (maxSteps: int)
            : Async<ControllerState * DistilledOutput> =
            async {
                let initialState = init goal budget
                return! runIntegrated services initialState maxSteps
            }
