    /// <summary>
    /// The Distiller compresses subgraphs into clean prose or structured JSON.
    /// </summary>
    module Distiller =

        /// <summary>
        /// Extract key insights from trace steps.
        /// </summary>
        let extractInsights (steps: WoTTraceStep list) : string list =
            steps
            |> List.choose (fun step ->
                step.Output
                |> Option.bind (fun o ->
                    if o.Length > 50 then Some(o.Substring(0, 50) + "...")
                    elif o.Length > 0 then Some o
                    else None))
            |> List.distinct
            |> List.truncate 5

        /// <summary>
        /// Distill trace steps into prose.
        /// </summary>
        let toProse (steps: WoTTraceStep list) (goal: string) : DistilledOutput =
            let insights = extractInsights steps

            let successfulSteps =
                steps
                |> List.filter (fun s ->
                    match s.Status with
                    | Completed _ -> true
                    | _ -> false)

            let insightLines =
                insights
                |> List.mapi (fun i insight -> sprintf "%d. %s" (i + 1) insight)
                |> String.concat "\n"

            let content =
                sprintf
                    "## Summary\nGoal: %s\nSteps completed: %d of %d\n\n### Key Insights\n%s\n"
                    goal
                    successfulSteps.Length
                    steps.Length
                    insightLines

            { Content = content
              Format = Prose
              SourceNodes = steps |> List.map (fun s -> s.NodeId)
              Confidence = float successfulSteps.Length / float (steps.Length |> max 1)
              KeyInsights = insights
              DistilledAt = DateTime.UtcNow }

        /// <summary>
        /// Distill trace steps into JSON.
        /// </summary>
        let toJson (steps: WoTTraceStep list) (goal: string) : DistilledOutput =
            let insights = extractInsights steps

            let successfulSteps =
                steps
                |> List.filter (fun s ->
                    match s.Status with
                    | Completed _ -> true
                    | _ -> false)

            let insightsJson =
                insights
                |> List.map (fun i -> sprintf "\"%s\"" (i.Replace("\"", "\\\"")))
                |> String.concat ", "

            let content =
                sprintf
                    "{\"goal\": \"%s\", \"stepsCompleted\": %d, \"totalSteps\": %d, \"insights\": [%s]}"
                    (goal.Replace("\"", "\\\""))
                    successfulSteps.Length
                    steps.Length
                    insightsJson

            { Content = content
              Format = Json
              SourceNodes = steps |> List.map (fun s -> s.NodeId)
              Confidence = float successfulSteps.Length / float (steps.Length |> max 1)
              KeyInsights = insights
              DistilledAt = DateTime.UtcNow }

        /// <summary>
        /// Distill trace steps into knowledge triples.
        /// </summary>
        let toTriples (steps: WoTTraceStep list) : DistilledOutput =
            let triples =
                steps
                |> List.collect (fun step ->
                    match step.Output with
                    | Some output when output.Length > 0 ->
                        [ ($"step:{step.NodeId}", "produced", output.Substring(0, min 100 output.Length)) ]
                    | _ -> [])

            let content =
                triples |> List.map (fun (s, p, o) -> $"{s} | {p} | {o}") |> String.concat "\n"

            { Content = content
              Format = Triples
              SourceNodes = steps |> List.map (fun s -> s.NodeId)
              Confidence = float triples.Length / float (steps.Length |> max 1)
              KeyInsights = triples |> List.map (fun (s, _, _) -> s) |> List.distinct
              DistilledAt = DateTime.UtcNow }

        /// <summary>
        /// Distill steps to the specified format.
        /// </summary>
        let distill (steps: WoTTraceStep list) (goal: string) (format: DistillFormat) : DistilledOutput =
            match format with
            | Prose -> toProse steps goal
            | Markdown -> toProse steps goal // Same as prose for now
            | Json -> toJson steps goal
            | Triples -> toTriples steps

