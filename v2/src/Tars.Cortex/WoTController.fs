    /// <summary>
    /// The Generator creates candidate thoughts, tool calls, and summaries.
    /// </summary>
    module Generator =

        /// <summary>
        /// Generate candidate thought nodes for expansion.
        /// </summary>
        let generateThoughts (prompt: string) (count: int) : Candidate list =
            // Generate diverse thought candidates
            let variations =
                [ "Let me analyze this step by step:"
                  "Breaking this down into components:"
                  "Considering the key factors:"
                  "From first principles:"
                  "Looking at this from multiple angles:" ]

            variations
            |> List.take (min count variations.Length)
            |> List.mapi (fun i variation ->
                let guidPart = Guid.NewGuid().ToString("N").Substring(0, 8)
                let id = sprintf "thought_%s" guidPart
                let genId = sprintf "gen_%d" i
                let promptText = sprintf "%s %s" variation prompt

                { Id = id
                  Node =
                    { Id = genId
                      Kind = Reason
                      Payload =
                        { Prompt = promptText
                          Hint = Some Smart }
                        :> obj
                      Metadata =
                        { Label = Some variation
                          Tags = [ "generated"; "thought" ]
                          Extra = Map.empty } }
                  Score = 1.0 - (float i * 0.1) // Decreasing scores
                  Rationale = sprintf "Approach: %s" variation
                  GeneratedAt = DateTime.UtcNow })

        /// <summary>
        /// Generate tool call candidates.
        /// </summary>
        let generateToolCalls (context: string) (availableTools: string list) : Candidate list =
            availableTools
            |> List.mapi (fun i tool ->
                let guidPart = Guid.NewGuid().ToString("N").Substring(0, 8)
                let id = sprintf "tool_%s" guidPart
                let toolId = sprintf "tool_%d" i
                let labelText = sprintf "Call %s" tool

                { Id = id
                  Node =
                    { Id = toolId
                      Kind = Tool
                      Payload = { Tool = tool; Args = Map.empty } :> obj
                      Metadata =
                        { Label = Some labelText
                          Tags = [ "generated"; "tool" ]
                          Extra = Map.empty } }
                  Score = 0.5
                  Rationale = sprintf "Tool '%s' may help with: %s" tool context
                  GeneratedAt = DateTime.UtcNow })

        /// <summary>
        /// Generate synthesis candidates from multiple inputs.
        /// </summary>
        let generateSynthesis (inputs: string list) : Candidate list =
            let inputSummary = inputs |> String.concat "; "
            let guidPart = Guid.NewGuid().ToString("N").Substring(0, 8)
            let id = sprintf "synth_%s" guidPart
            let promptText = sprintf "Synthesize these insights: %s" inputSummary

            [ { Id = id
                Node =
                  { Id = "synthesis"
                    Kind = Reason
                    Payload =
                      { Prompt = promptText
                        Hint = Some Smart }
                      :> obj
                    Metadata =
                      { Label = Some "Synthesis"
                        Tags = [ "synthesis" ]
                        Extra = Map.empty } }
                Score = 0.9
                Rationale = "Combine multiple perspectives into coherent output"
                GeneratedAt = DateTime.UtcNow } ]

        /// <summary>
        /// Generate best candidate set with diversity.
        /// </summary>
        let generate (prompt: string) (count: int) (includeTools: string list) : GeneratorOutput =
            let thoughts = generateThoughts prompt count
            let tools = generateToolCalls prompt includeTools
            let all = thoughts @ tools |> List.sortByDescending (fun c -> c.Score)

            let diversityScore =
                let kinds = all |> List.map (fun c -> c.Node.Kind) |> List.distinct |> List.length
                float kinds / float (all.Length |> max 1)

            { Candidates = all
              BestCandidate = all |> List.tryHead
              DiversityScore = diversityScore }

