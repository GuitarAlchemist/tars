    /// <summary>
    /// The Router decides: expand, merge, rollback, escalate, or finalize.
    /// </summary>
    module Router =

        /// <summary>
        /// Analyze the current state and make a routing decision.
        /// </summary>
        let decide (context: RoutingContext) (critique: Critique option) : RouterDecision =
            let failCount =
                context.FailedAttempts
                |> Map.tryFind context.CurrentNode.Id
                |> Option.defaultValue 0

            // Check for excessive failures
            if failCount >= 3 then
                Escalate(context.CurrentNode.Id, "Max retries exceeded", 0.8)

            // Use critique if available
            elif critique.IsSome then
                critique.Value.Recommendation

            // Check budget
            elif context.RemainingBudget.MaxTokens.IsSome then
                match context.RemainingBudget.MaxTokens with
                | Some tokens when tokens < 1000<token> -> Finalize(context.CurrentNode.Id, 0.6)
                | _ -> Expand(context.CurrentNode.Id, "Budget available")

            // Default: continue
            else
                Expand(context.CurrentNode.Id, "Default expansion")

        /// <summary>
        /// Check if human escalation is needed.
        /// </summary>
        let needsHumanInput (decision: RouterDecision) : bool =
            match decision with
            | Escalate _ -> true
            | _ -> false

        /// <summary>
        /// Get a human-readable description of a decision.
        /// </summary>
        let describeDecision (decision: RouterDecision) : string =
            match decision with
            | Expand(nodeId, reason) -> sprintf "EXPAND: Continue from '%s' - %s" nodeId reason
            | Merge(nodeIds, target) ->
                let joined = nodeIds |> String.concat ", "
                sprintf "MERGE: Combine [%s] into '%s'" joined target
            | Rollback(toNodeId, reason) -> sprintf "ROLLBACK: Return to '%s' - %s" toNodeId reason
            | Escalate(nodeId, reason, urgency) ->
                sprintf "ESCALATE: Need human review at '%s' (urgency: %.2f) - %s" nodeId urgency reason
            | Finalize(nodeId, confidence) ->
                sprintf "FINALIZE: Mark '%s' as complete (confidence: %.2f)" nodeId confidence
            | CallTool(tool, _) -> sprintf "TOOL: Execute '%s'" tool
            | ApplyPolicy(policy, target) -> sprintf "POLICY: Apply '%s' to '%s'" policy target
            | Retry(nodeId, attempts) -> sprintf "RETRY: Retry '%s' (up to %d attempts)" nodeId attempts

