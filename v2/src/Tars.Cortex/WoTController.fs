    /// <summary>
    /// The Critic evaluates relevance, correctness, risk, and policy compliance.
    /// </summary>
    module Critic =

        /// <summary>
        /// Evaluate a node's relevance to the goal.
        /// </summary>
        let evaluateRelevance (node: WoTNode) (goal: string) : CriticDimension =
            // Simple keyword overlap for relevance
            let goalWords =
                goal.ToLower().Split([| ' '; ','; '.'; ';' |], StringSplitOptions.RemoveEmptyEntries)
                |> Set.ofArray

            let nodeContent =
                match node.Payload with
                | :? ReasonPayload as p -> p.Prompt
                | :? ToolPayload as p -> p.Tool
                | _ -> ""

            let nodeWords =
                nodeContent.ToLower().Split([| ' '; ','; '.'; ';' |], StringSplitOptions.RemoveEmptyEntries)
                |> Set.ofArray

            let overlap = Set.intersect goalWords nodeWords |> Set.count |> float
            let score = (overlap / float (Set.count goalWords |> max 1)) |> min 1.0

            Relevance(score, $"Keyword overlap: {overlap} of {Set.count goalWords}")

        /// <summary>
        /// Evaluate risk factors.
        /// </summary>
        let evaluateRisk (node: WoTNode) : CriticDimension =
            let factors = ResizeArray<string>()
            let mutable level = 0.0

            match node.Kind with
            | Tool ->
                factors.Add("External side effects possible")
                level <- level + 0.3
            | Memory ->
                factors.Add("State modification")
                level <- level + 0.2
            | _ -> ()

            // Check for dangerous patterns in payloads
            match node.Payload with
            | :? ToolPayload as p ->
                if p.Tool.Contains("delete") || p.Tool.Contains("remove") then
                    factors.Add("Destructive operation")
                    level <- level + 0.4

                if p.Tool.Contains("execute") || p.Tool.Contains("run") then
                    factors.Add("Code execution")
                    level <- level + 0.3
            | _ -> ()

            Risk(level |> min 1.0, factors |> Seq.toList)

        /// <summary>
        /// Evaluate policy compliance.
        /// </summary>
        let evaluatePolicyCompliance (node: WoTNode) (policies: string list) : CriticDimension =
            // Simple policy checks
            let violations = ResizeArray<string>()

            policies
            |> List.iter (fun policy ->
                match policy with
                | "require_tool_confirmation" when node.Kind = Tool ->
                    // This would trigger human review
                    ()
                | "max_tokens" ->
                    // Check token budget
                    ()
                | _ -> ())

            PolicyCompliance(violations.Count = 0, violations |> Seq.toList)

        /// <summary>
        /// Create a complete critique of a node.
        /// </summary>
        let critique (node: WoTNode) (goal: string) (policies: string list) : Critique =
            let dimensions =
                [ evaluateRelevance node goal
                  evaluateRisk node
                  evaluatePolicyCompliance node policies ]

            // Calculate overall score
            let scores =
                dimensions
                |> List.choose (fun d ->
                    match d with
                    | Relevance(s, _) -> Some s
                    | Quality(s, _) -> Some s
                    | Risk(r, _) -> Some(1.0 - r) // Invert risk
                    | PolicyCompliance(passed, _) -> Some(if passed then 1.0 else 0.0)
                    | Correctness(s, _) -> Some s)

            let overall = if scores.IsEmpty then 0.5 else scores |> List.average

            let recommendation =
                if overall < 0.3 then Rollback(node.Id, "Low quality score")
                elif overall < 0.6 then Retry(node.Id, 2)
                elif overall >= 0.9 then Finalize(node.Id, overall)
                else Expand(node.Id, "Continue exploration")

            { NodeId = node.Id
              Dimensions = dimensions
              OverallScore = overall
              Recommendation = recommendation
              Reasoning = $"Evaluated {dimensions.Length} dimensions, overall score: {overall:F2}"
              CritiquedAt = DateTime.UtcNow }

