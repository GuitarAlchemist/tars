namespace TaskManager.Tests.Unit

open Xunit
open FsUnit.Xunit
open System
open TaskManager.Core.Domain

module DomainTests =

    [<Fact>]
    let `User creation should have valid defaults` () =
        // Arrange & Act
        let user = {
            Id = Guid.NewGuid()
            Email = "test@example.com"
            FirstName = Some "Test"
            LastName = Some "User"
            CreatedAt = DateTime.UtcNow
            IsActive = true
        }
        
        // Assert
        user.Id |> should not' (equal Guid.Empty)
        user.Email |> should equal "test@example.com"
        user.IsActive |> should equal true

    [<Fact>]
    let `Entity creation should be valid` () =
        // Arrange & Act
        let entity = {
            Id = Guid.NewGuid()
            Name = "Test Entity"
            Description = Some "Test Description"
            OwnerId = Guid.NewGuid()
            Data = Map.empty
            CreatedAt = DateTime.UtcNow
            UpdatedAt = DateTime.UtcNow
        }
        
        // Assert
        entity.Id |> should not' (equal Guid.Empty)
        entity.Name |> should equal "Test Entity"
        entity.Data |> should be Empty

