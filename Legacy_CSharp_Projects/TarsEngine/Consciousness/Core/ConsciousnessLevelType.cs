namespace TarsEngine.Consciousness.Core;

/// <summary>
/// Represents the different levels of consciousness
/// </summary>
public enum ConsciousnessLevelType
{
    /// <summary>
    /// Basic level of consciousness with minimal awareness
    /// </summary>
    Basic,
        
    /// <summary>
    /// Aware level of consciousness with environmental awareness
    /// </summary>
    Aware,
        
    /// <summary>
    /// Self-aware level of consciousness with recognition of self
    /// </summary>
    SelfAware,
        
    /// <summary>
    /// Reflective level of consciousness with ability to reflect on thoughts
    /// </summary>
    Reflective,
        
    /// <summary>
    /// Fully conscious level with integrated awareness
    /// </summary>
    Conscious
}