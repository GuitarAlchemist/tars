        member _.ExecuteAsync(options: CommandOptions) =
            Task.Run(fun () ->
                try
                    logger.LogInformation("Starting TARS AI HTTP Server")
                    
                    let port = 
                        match options.Arguments with
                        | [] -> 7777
                        | portStr :: _ -> 
                            match Int32.TryParse(portStr) with
                            | (true, p) -> p
                            | _ -> 7777
                    
