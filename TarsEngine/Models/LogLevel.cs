namespace TarsEngine.Models;

/// <summary>
/// Represents the level of a log entry
/// </summary>
public enum LogLevel
{
    /// <summary>
    /// Trace level
    /// </summary>
    Trace,

    /// <summary>
    /// Debug level
    /// </summary>
    Debug,

    /// <summary>
    /// Information level
    /// </summary>
    Information,

    /// <summary>
    /// Warning level
    /// </summary>
    Warning,

    /// <summary>
    /// Error level
    /// </summary>
    Error,

    /// <summary>
    /// Critical level
    /// </summary>
    Critical
}
