namespace TarsEngine.Consciousness.Core;

/// <summary>
/// Types of thought processes
/// </summary>
public enum ThoughtType
{
    /// <summary>
    /// Analytical thought (logical, systematic)
    /// </summary>
    Analytical,
    
    /// <summary>
    /// Creative thought (imaginative, innovative)
    /// </summary>
    Creative,
    
    /// <summary>
    /// Reflective thought (introspective, evaluative)
    /// </summary>
    Reflective,
    
    /// <summary>
    /// Intuitive thought (gut feeling, instinctive)
    /// </summary>
    Intuitive,
    
    /// <summary>
    /// Critical thought (evaluative, judgmental)
    /// </summary>
    Critical,
    
    /// <summary>
    /// Practical thought (problem-solving, action-oriented)
    /// </summary>
    Practical,
    
    /// <summary>
    /// Abstract thought (conceptual, theoretical)
    /// </summary>
    Abstract,
    
    /// <summary>
    /// Concrete thought (specific, detailed)
    /// </summary>
    Concrete,
    
    /// <summary>
    /// Divergent thought (exploring multiple possibilities)
    /// </summary>
    Divergent,
    
    /// <summary>
    /// Convergent thought (narrowing to a solution)
    /// </summary>
    Convergent,
    
    /// <summary>
    /// Meta thought (thinking about thinking)
    /// </summary>
    Meta,
    
    /// <summary>
    /// Automatic thought (habitual, unconscious)
    /// </summary>
    Automatic,
    
    /// <summary>
    /// Background thought (low priority, peripheral)
    /// </summary>
    Background
}
