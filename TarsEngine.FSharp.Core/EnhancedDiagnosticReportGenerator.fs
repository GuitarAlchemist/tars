            sb.AppendLine() |> ignore
            sb.AppendLine("| Agent | Specialization | Capabilities | Learning Rate | Collaboration |") |> ignore
            sb.AppendLine("|-------|----------------|--------------|---------------|---------------|") |> ignore

            for agent in availableAgents do
